# -*- coding: utf-8 -*-

import math


def ceil(number):
    return math.ceil(number)


def floor(number):
    return math.floor(number)


def divide(dividend, divisor):
    """
    :param dividend: 被除数
    :param divisor: 除数
    """

    if divisor == 0.:
        return 0.
    else:
        dividend / divisor



